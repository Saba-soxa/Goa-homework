# N3

def area_or_perimeter(l , w):
    if l == w:
        tot = l * w
    else:
        tot = l * 2 + w * 2
    return tot


# N4

def make_negative(number):
    if number > 0:
        return -number
    return number


# N5

def opposite(number):
    return - number
